    @Nullable
    public BlockState getStateForPlacement(BlockPlaceContext context) {
        FluidState fluidstate = context.getLevel().getFluidState(context.getClickedPos());


        BlockState blockstate = this.defaultBlockState();
        LevelReader levelreader = context.getLevel();
        BlockPos blockpos = context.getClickedPos();
        Direction[] adirection = context.getNearestLookingDirections();


        if(context.getClickedFace().getAxis().isHorizontal())
            return blockstate.setValue(FACING,context.getClickedFace());

        return blockstate.setValue(FACING,context.getPlayer().getDirection().getOpposite());
    }


    @Override
    public VoxelShape getShape(BlockState p_60555_, BlockGetter p_60556_, BlockPos p_60557_, CollisionContext p_60558_) {
        return CDDShapes.DIAGONAL_METAL_SUPPORT.get(p_60555_.getValue(FACING).getOpposite());
    }
